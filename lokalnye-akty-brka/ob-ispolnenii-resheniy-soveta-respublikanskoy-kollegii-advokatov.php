<p>
	ПОСТАНОВЛЯЕТ:
</p>
<p>
	1. Обратить внимание советов территориальных коллегий адвокатов на недопустимость неисполнения постановлений совета Республиканской коллегии адвокатов.
</p>
<p>
	 2. Рекомендовать советам территориальных коллегий адвокатов ввести практику отчетов на общих собраниях (конференциях) о проделанной работе членов совета территориальной коллегии адвокатов.
</p>
<p>
