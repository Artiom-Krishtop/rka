</p>
<p>
	 4. Проекты постановлений общих собраний (конференций) адвокатов должны доводиться до сведения адвокатов не позднее, чем за семь дней до начала работы собрания (конференции).
</p>
<p>
	 5. Советам территориальных коллегий адвокатов в срок до 1 июля разработать и принять программу материально-технического обеспечения коллегии адвокатов на период до 2015 года.
</p>
<p>
	 6. Советам территориальных коллегий адвокатов планировать проведение общих собраний (конференций) адвокатов не позднее 1 марта 2014 года с установлением фиксированного взноса на содержание территориальной и Республиканской коллегий адвокатов с 1 января наступившего года.
</p>
<p>
	 7. Республиканской коллегии адвокатов в срок о 1 ноября 2013 года разработать общие подходы для определения фиксированных взносов на содержание территориальных коллегий адвокатов.
</p>
<p>
	 8. Контроль за исполнением данного постановления возложить на Республиканскую коллегию адвокатов.
