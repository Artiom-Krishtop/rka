<p>&nbsp;</p>

<p align="center">Обстоятельства финансовой операции,</p>

<p align="center">определяющие степень риска
как высокую</p>

<p>&nbsp;</p>

<p>Высокий риск того, что
финансовая операция осуществляется с целью легализации доходов, полученных
преступным путем, финансирования террористической деятельности и
распространения оружия массового поражения, имеется в случаях, когда:</p>

<p>1. Участник финансовой
операции зарегистрирован (имеет место жительства) или место нахождения, место
осуществления деятельности в государстве или на территории, которое не
участвует в международном сотрудничестве в сфере предотвращения легализации
доходов, полученных преступным путем, финансирования террористической
деятельности и финансирования распространения оружия массового поражения, либо
не выполняет рекомендации ФАТФ.</p>

<p>2. Участник финансовой
операции использует счет в банке, зарегистрированном в государстве или на
территории, которые не участвуют в международном сотрудничестве в сфере
предотвращения легализации доходов, полученных преступным путем, финансирования
террористической деятельности и финансирования распространения оружия массового
поражения, либо не выполняет рекомендации ФАТФ.</p>

<p>3. Участник финансовой
операции или выгодоприобретатель включен в перечень организаций и физических
лиц, причастных к террористической деятельности (далее – Перечень).
Формирование и ведение перечня, доведение перечня до сведения лиц,
осуществляющих финансовые операции, и до органа финансового мониторинга,
осуществляет уполномоченный орган, устанавливаемый Советом Министров Республики
Беларусь. </p>

<p>Перечень организаций и
физических лиц, причастных к террористической деятельности, а также
организаций, бенефициарными владельцами которых являются физические лица,
включенные в этот перечень, публикуется на сайте Комитета государственной безопасности
Республики Беларусь (<a href="http://kgb.by/ru/perechen-inf-ru/)">http://kgb.by/ru/perechen-inf-ru/)</a>.</p>

<p>4. Физическое лицо, которое
является бенефициарным владельцем организации, являющейся участником финансовой
операции либо выгодоприобретателем, включено в Перечень организаций и физических
лиц, причастных к террористической деятельности.</p>

<p>5. Клиент является
некоммерческой организацией, и финансовая операция не соответствует целям
деятельности клиента.</p>

<p>6. Финансовая операция
осуществляется клиентом неоднократно в целях уклонения от регистрации в
специальном формуляре.</p>

<p>7. Клиент является физическим
лицом и участвует в финансовой операции, сумма которой равна или превышает 500
базовых величин. При этом клиент использует счет в банке, зарегистрированном в
государстве или на территории, которое не участвует в международном
сотрудничестве в сфере предотвращения легализации доходов, полученных
преступным путем, финансирования террористической деятельности и
распространения оружия массового поражения, либо не выполняет рекомендации ФАТФ
(приложение 6).</p>

<p>8. Клиент является
организацией или индивидуальным предпринимателем и участвует в финансовой
операции, сумма которой равна или превышает 1000 базовых величин.
При этом использует счет в банке, зарегистрированном в государстве или на
территории, которое не участвует в международном сотрудничестве в сфере
предотвращения легализации доходов, полученных преступным путем, финансирования
террористической деятельности и распространения оружия массового поражения,
