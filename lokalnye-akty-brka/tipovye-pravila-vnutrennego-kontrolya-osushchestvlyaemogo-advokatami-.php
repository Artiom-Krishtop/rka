<p>&nbsp;</p>

<p align="center">Признаки подозрительных финансовых операций</p>

<p align="center">и критерии их выявления</p>

<p>&nbsp;Финансовая операция считается
вызывающей подозрения в том, что имеет место легализация доходов, полученных
преступным путем, финансирование террористической деятельности и
распространения оружия массового поражения, если:</p>

<p>1. Участник финансовой
операции зарегистрирован или имеет место нахождения в оффшорной зоне.</p>

<p>2. Участник финансовой
операции представляет вызывающую подозрение информацию, проверить которую
невозможно либо проверка которой является слишком дорогостоящей.</p>

<p>3. Адвокат не может
осуществить связь с участником финансовой операции по указанным им адресам и
телефонам.</p>

<p>4. Участник финансовой
операции немотивированно отказывает Адвокату в представлении сведений, которые
оно запрашивает, либо неоправданно задерживает представление таких сведений.</p>

<p>5. Участник финансовой
операции проявляет чрезмерную озабоченность вопросами конфиденциальности при
оказании соответствующей юридической помощи.</p>

<p>6. Участник финансовой
операции представляет документы (их копии), вызывающие сомнение в их
подлинности (достоверности).</p>

<p>7. Адвокат не может установить
контрагентов участника финансовой операции либо устанавливает несоответствие
сведений о стороне по сделке, представленных клиентом, уже имеющейся у Адвоката
информации.</p>

<p>8. Участник финансовой
операции систематически предоставляет или получает безвозмездную (спонсорскую)
помощь в рамках договоров между организациями и (или) индивидуальными
предпринимателями.</p>

<p>9. Финансовая операция не
соответствует характеру деятельности участника финансовой операции.</p>

<p>10. Участник финансовой
операции необоснованно дробит суммы однотипных финансовых операций (одного
вида), осуществляемых им в течение анализируемого периода. При этом не
учитываются операции по размещению денежных средств на счета, во вклады
(депозиты), а также по снятию денежных средств со счетов, вкладов (депозитов).</p>

<p>11. Участник финансовой
операции хотя бы один раз либо неоднократно в течение анализируемого периода
осуществляет перевод денежных средств за границу:</p>

<p>- для осуществления
деятельности на валютном рынке Форекс;</p>

<p>- осуществления финансовых
операций, связанных с приобретением либо продажей ценных бумаг и других
высоколиквидных активов (в том числе слитков драгоценных металлов);.</p>

<p>- при условии, что информация
в отношении данного участника финансовой операции не позволяет определить
источник происхождения денежных средств.</p>

<p>12. Участник финансовой
операции хотя бы один раз в течение анализируемого периода получил заем, кредит
из-за границы.</p>

<p>13. В финансовой операции
клиента отсутствует явный экономический смысл.</p>

<p>14. Участник финансовой
операции разово продает (покупает) большое количество финансовых инструментов,
ценных бумаг, не обращающихся на организованном рынке ценных бумаг, по ценам,
существенно отличающимся от рыночных, при условии, что:</p>

<p>- участник финансовой
операции не является профессиональным участником рынка ценных бумаг,</p>

<p>- ценные бумаги не передаются
участнику финансовой операции в погашение задолженности контрагента перед
участником финансовой операции.</p>

<p>15. Участник финансовой
операции систематически осуществляет финансовые операции по покупке с
последующей продажей ценных бумаг, не имеющих котировок и не обращающихся на
организованном рынке ценных бумаг. При этом доход от реализации ценных бумаг
направляется на приобретение высоколиквидных активов, обращающихся на
организованном рынке ценных бумаг.</p>

<p>16. Участник финансовой
операции систематически осуществляет финансовые операции, при которых один и
тот же предмет сделки продается и затем выкупается в рамках осуществления
сделок с одной и той же стороной, за исключением сделок РЕПО с ценными
бумагами.</p>

<p>17. Участник финансовой
операции настаивает на срочном проведении финансовой операции, не имея
достаточных обоснований.</p>

<p>18. Непосредственно перед
началом реализации финансовой операции участник вносит значительные изменения в
ранее согласованную схему ее проведения, касающиеся направления движения
денежных средств или иного имущества.</p>

<p>19. Участник финансовой
операции систематически изменяет порядок исполнения договорных обязательств.</p>

<p>20. Участник финансовой
операции предоставляет (получает) беспроцентный заем, а также заем, кредит на
условиях, предусматривающих получение (уплату) процентов, иных вознаграждений
(платежей) в размере, существенно отличающемся от обычной рыночной практики.</p>

<p>21. Участник финансовой
операции - резидент возвращает ранее полученный аванс нерезиденту по договору
поставки товаров (выполнения работ, оказания услуг) в случаях, когда такая
финансовая операция носит систематический характер.</p>

<p>22. Участник финансовой
операции хотя бы один раз в течение анализируемого периода вносит наличные
денежные средства, связанные с приобретением имущества, если информация,
имеющаяся у Адвоката, в отношении данного участника не позволяет определить
источник происхождения денежных средств.</p>

<p>23. Основные условия
внешнеторгового договора или порядка его исполнения существенно отличаются от
общепринятой внешнеторговой практики осуществления таких сделок и (или)
международных правил, применяемых к аналогичным сделкам.</p>

<p>24. Участник финансовой
операции систематически осуществляет финансовые операции без явного
экономического смысла, которые связаны с поступлением денежных средств от
нескольких контрагентов и сопровождаются последующим перечислением этих
денежных средств в полном или подавляющем объеме, в том числе с покупкой или
конверсией иностранной валюты.</p>

<p>25. Участник финансовой
операции осуществляет несколько финансовых операций, каждая из которых в
отдельности не является подозрительной, но по совокупности признаков такие
финансовые операции вызывают подозрения, что они осуществляются в целях уклонения
от уплаты налогов, сборов (пошлин); занижения тарифных платежей за счет
недостоверного декларирования ввозимых товаров; легализации доходов, полученных
преступным путем.</p>

<p>26. Участник финансовой
операции ведет себя необычным образом при открытии счета или осуществлении
финансовой операции от своего имени и в своих интересах, что дает основания
полагать, что он действует в интересах иного лица, либо намерения клиента
открыть счет (счета), электронный кошелек (электронные кошельки) могут быть
связаны с получением и (или) легализацией доходов, полученных преступным путем.</p>

<p>27. У Адвоката возникают
подозрения, что участник финансовой операции совершает ее в целях
финансирования террористической деятельности и (или) финансирования
распространения оружия массового поражения.</p>

<p>28. Участник финансовой
операции осуществляет финансовую операцию с недвижимым имуществом до истечения
одного года с момента государственной регистрации этого участника.</p>

<p>29. Участник финансовой
операции пытается осуществить финансовую операцию с недвижимым имуществом, на
которое наложено ограничение (обременение), запрещающее по своему правовому
характеру правообладателю распоряжаться объектом недвижимого имущества.</p>

<p>30. Одним из участников
финансовой операции с недвижимым имуществом является нерезидент.</p>

<p>31. Заключается
предварительный договор, который предусматривает как оплату аванса (задатка),
так и выплату сумм, которые будут предусмотрены основным договором, до
заключения (подписания) основного договора;</p>

<p>32. Заключаются соглашения о
расторжении возмездных договоров об отчуждении имущества, в которых не
предусматривается возврат сторонам переданных по договору имущества и денежных
средств.</p>

<p>33. По предварительному
договору выплачивается задаток в значительных размерах.</p>

<p>34. Действительная стоимость
предмета сделки явно не соответствует ее договорной стоимости (кроме объекта
недвижимости, прав на него), то есть расхождение договорной стоимости предмета
сделки, составляет 25 и более процентов от действительной стоимости;</p>

<p>35. Стоимость объекта недвижимости
(прав на него) существенно отличается от обычной рыночной стоимости такого
объекта (прав на него),
то есть расхождение стоимости объекта недвижимости (прав на него), составляет  25 и более процентов от обычной рыночной
стоимости;</p>

<p>36. Однотипные финансовые
операции осуществляет представитель от имени трех и более участников финансовых
операций и такие операции сопровождаются внесением либо получением наличных
денежных средств или высоколиквидных финансовых инструментов.</p>

<p>37. Расчеты по финансовой операции
с недвижимым (движимым) имуществом на сумму более 100 базовых величин
осуществляются в наличной форме.</p>

<p>38. Участник финансовой
операции осуществляет финансовую операцию с нарушением установленного
законодательством порядка документального оформления, в том числе с нарушением
требования о нотариальном удостоверении или государственной регистрации.</p>

<p>39. Участник финансовой
операции неоднократно проводит однотипные финансовые операции в целях дробления
суммы необычно крупной сделки и (или) уклонения от регистрации в специальном
формуляре.</p>

<p>40. Участник финансовой
операции снижает сумму сделки в ходе финансовой операции в целях уклонения от
регистрации в специальном формуляре.</p>

<p>41. Осуществление клиентом
финансовой операции, если по совокупности имеющейся информации возникают
подозрения, что они осуществляются в целях незаконного оборота наркотических
средств, психотропных веществ, их аналогов и прекурсоров либо на основании критериев, определяемых в том числе
международными организациями.</p>

<p>42. Систематическое
осуществление финансовых операций между аффилированными юридическими лицами.</p>

<p>43. осуществление клиентом
финансовых операций, связанных с приобретением или отчуждением цифровых знаков
(токенов) за белорусские рубли, иностранную валюту, электронные деньги у
нерезидентов без участия резидента Парка высоких технологий.</p>

<p>44. Осуществление финансовой
операции с одним и тем же недвижимым имуществом два раза и более в течение
одного года, кроме наследования недвижимого имущества. </p>

<p>45. Иные признаки, при
наличии которых у Адвоката возникли подозрения, что финансовая операция
осуществляется в целях легализации доходов, полученных преступным путем,
финансирования террористической деятельности и финансирования распространения
оружия массового поражения.</p>

