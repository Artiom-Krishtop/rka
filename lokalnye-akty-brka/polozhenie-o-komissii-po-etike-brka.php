<p align="center">
	 1.Общие положения
</p>
<p>
	 1.1. Комиссия по соблюдению адвокатами Правил профессиональной этики адвоката и качеству оказания юридической помощи (далее – Комиссия по этике) создается решением совета Белорусской республиканской коллегии адвокатов в соответствии с пунктом 19 Устава и является коллегиальным органом Белорусской республиканской коллегии адвокатов.
</p>
<p>
	 1.2. Целями Комиссии по этике являются:
</p>
<p>
	 утверждение среди адвокатов принципов уважительного отношения к закону адвокатской профессии, содействие сохранению и повышению ее престижа;
</p>
<p>
	 содействие в реализации принципа самоуправления адвокатуры и обеспечение повышения качества и доступности юридической помощи;
</p>
<p>
	 формирование единых подходов к соблюдению адвокатами Правил профессиональной этики адвоката;
</p>
<p>
	 содействие в воспитании молодого поколения адвокатов;
</p>
<p>
	 оказание помощи и содействия советам территориальных коллегий адвокатов в выполнении возложенных на задач;
</p>
<p>
	 предупреждение, выявление и профилактика нарушений в сфере профессиональной деятельности адвокатов;
</p>
<p>
	 1.3. Основными задачами Комиссии по этике являются:
</p>
<p>
	 дача разъяснений и заключений по вопросам применения Правил профессиональной этики адвоката и защиты прав адвокатов;
</p>
<p>
	 дача заключений по результатам рассмотрения материалов по жалобам, поступившим в Белорусскую республиканскую коллегию адвокатов;
</p>
<p>
	 мониторинг соблюдения адвокатами требований законодательства об адвокатуре и адвокатской деятельности, Правил профессиональной этики адвоката и качества оказываемой ими юридической помощи;
</p>
<p>
	 обобщение дисциплинарной практики территориальных коллегий адвокатов и практики применения Правил профессиональной этики адвоката;
</p>
<p>
	 разрешение внутрикорпоративных конфликтов среди адвокатов.
</p>
<p align="center">
