<p align="center">
	 2.Полномочия Комиссии по этике
</p>
<p>
	 2.1. Полномочиями Комиссии по этике являются:
</p>
<p>
	 рассмотрение обращений адвокатов, председателей, заместителей, советов территориальных коллегий адвокатов по вопросам применения Правил профессиональной этики адвоката и защиты прав адвокатов, дача разъяснений, заключений о наличии (отсутствии) в действиях адвоката нарушения Правил профессиональной этики, наличии (отсутствии) нарушения прав адвокатов и о мерах, которые следует принять с целью их устранения;
</p>
<p>
	 рассмотрение по поручению председателя (заместителя председателя), совета Белорусской республиканской коллегии адвокатов материалов по жалобам на действия председателей, заместителей, членов советов Белорусской республиканской и территориальных коллегий адвокатов, представлениям государственных органов, должностных лиц, частным определениям суда о привлечении их к дисциплинарной ответственности (далее - жалоба (представление, определение)), дача заключения о наличии либо отсутствии в их действиях признаков дисциплинарного проступка;
</p>
<p>
	 рассмотрение по поручению председателя (заместителя председателя), совета Белорусской республиканской коллегии адвокатов материалов по жалобам на решения органов управления территориальных коллегий адвокатов, дача заключения по результатам их рассмотрения;
</p>
<p>
	 проведение проверок (мониторинга) соблюдения адвокатами требований Правил профессиональной этики адвоката и качества оказываемой ими юридической помощи, в том числе, соответствия оплаты юридической помощи ее объему, сложности, качеству (далее - мониторинг), дача заключения по результатам мониторинга;
</p>
<p>
	 разработка проектов Методических рекомендаций, иных локальных актов по вопросам применения Правил профессиональной этики адвоката;
</p>
<p>
	 обобщение практики применения Правил профессиональной этики адвоката, дисциплинарной практики территориальных коллегий адвокатов;
</p>
<p>
	 взаимодействие с комиссиями по соблюдению адвокатами Правил профессиональной этики адвоката и качеству оказания юридической помощи территориальных коллегий адвокатов (далее – комиссии по этике территориальных коллегий адвокатов), комиссиями по контролю за качеством оказания юридической помощи адвокатами юридических консультаций (далее - комиссии по качеству юридических консультаций) с целью достижения поставленных задач;
</p>
<p>
	 рассмотрение внутрикорпоративных конфликтов среди адвокатов, дача рекомендаций по урегулированию конфликта;
</p>
<p>
	 2.2. Комиссия по этике осуществляет свою деятельность в соответствии с законодательством об адвокатуре и адвокатской деятельности, уставом Белорусской республиканской коллегии адвокатов, локальными актами Белорусской республиканской коллегии адвокатов и настоящим Положением.
</p>
<p>
	 В случаях, когда вопросы профессиональной этики адвоката не урегулированы законодательством и локальными актами, Комиссия по этике руководствуется обычаями и традициями, сложившимися в адвокатуре.
</p>
<p>
	 2.3. Деятельность Комиссии по этике основывается на принципах законности, справедливости, объективности, независимости и беспристрастности.
</p>
<p align="center">
	 3. Состав Комиссии по этике
</p>
<p>
	 3.1. Состав Комиссии по этике формируется сроком на четыре года из адвокатов, представленных советами территориальных коллегий в количестве двух представителей от каждой коллегии, и заместителя председателя Белорусской республиканской коллегии адвокатов.
</p>
<p>
	 Председателем Комиссии по этике является заместитель председателя Белорусской республиканской коллегии адвокатов по должности.
</p>
<p>
	 Заместитель председателя Комиссии по этике избирается членами Комиссии из ее членов на срок работы Комиссии по этике.
</p>
<p>
	 Члены Комиссии по этике утверждаются на заседании совета Белорусской республиканской коллегии адвокатов с их согласия из числа наиболее квалифицированных, опытных и пользующихся авторитетом адвокатов.
</p>
<p>
	 3.2. Председатель Комиссии по этике:
</p>
<p>
	 разрабатывает план работы Комиссии по этике на год, который утверждается советом Белорусской республиканской коллегии адвокатов;
</p>
<p>
	 организует работу Комиссии по этике в соответствии с планом работы и поручениями председателя Белорусской республиканской коллегии адвокатов;
</p>
<p>
	 организует проведение Комиссией по этике мониторинга деятельности адвокатов, юридических консультаций территориальных коллегий адвокатов в соответствии с планом работы либо поручениями председателя Белорусской республиканской коллегии адвокатов;
</p>
<p>
	 координирует работу комиссий по этике территориальных коллегий адвокатов, комиссий по качеству юридических консультаций;
</p>
<p>
	 дает поручения членам Комиссии по этике;
</p>
<p>
	 поручает проведение мониторинга комиссии по этике территориальных коллегий адвокатов, комиссии по качеству юридических консультаций;
</p>
<p>
	 запрашивает в территориальных коллегиях адвокатов, иных организациях материалы, необходимые для проведения проверки по жалобам и обобщения практики применения Правил профессиональной этики адвоката и дисциплинарной практики;
</p>
<p>
	 истребует у адвокатов, председателей, заместителей, членов советов Белорусской республиканской и территориальных коллегий адвокатов объяснения по поступившим на них жалобам (представлениям, определениям) и обращениям;
</p>
<p>
	 уведомляет членов Комиссии по этике и заинтересованных лиц о месте, времени и повестке дня заседаний;
</p>
<p>
	 раз в год направляет в совет Белорусской республиканской коллегии адвокатов информацию о деятельности Комиссии по этике.
</p>
<p>
	 3.3. Заместитель председателя Комиссии по этике:
</p>
<p>
	 выполняет обязанности председателя Комиссии по этике в случае его отсутствия;
</p>
<p>
	 выполняет другие обязанности по поручению председателя Комиссии по этике.
</p>
<p>
	 3.4. Члены Комиссии по этике вправе:
</p>
<p>
	 участвовать в проведении мониторинга и проверок по соблюдению адвокатами Правил профессиональной этики адвоката и качеству оказания юридической помощи;
</p>
<p>
	 знакомиться со всеми документами и материалами, необходимыми для решения вопросов, входящих в полномочия Комиссии по этике;
</p>
<p>
	 участвовать в принятии решений по всем вопросам, входящим в компетенцию Комиссии по этике;
</p>
<p>
	 получать вознаграждение за работу в Комиссии по этике в размере, определенном советом Белорусской республиканской коллегии адвокатов.
</p>
<p>
	 3.5. Члены Комиссии по этике обязаны:
</p>
<p>
	 Участвовать в работе Комиссии по этике;
</p>
<p>
	 уведомлять председателя Комиссии по этике о невозможности принять участие в заседании;
</p>
<p>
	 выполнять поручения председателя Комиссии по этике;
</p>
<p>
	 не разглашать сведения, составляющие адвокатскую тайну, которые стали им известны в результате участия в работе Комиссии по этике;
</p>
<p>
	 3.6. Полномочия членов Комиссии по этике прекращаются решением совета Белорусской республиканской коллегии адвокатов:
</p>
<p>
	 в связи с истечением срока, на который формировалась Комиссия по этике;
</p>
<p>
	 по заявлению члена Комиссии по этике;
</p>
<p>
	 при невозможности исполнения обязанностей члена Комиссии по этике более шести месяцев подряд;
</p>
<p>
	 в связи с прекращением членства в территориальной коллегии адвокатов члена Комиссии по этике.
</p>
<p>
	 в связи с ненадлежащим исполнением членом Комиссии по этике своих обязанностей.
</p>
<p align="center">
	 4. Заседания Комиссии по этике
</p>
<p>
	 4.1. Заседания Комиссии по этике созываются председателем Комиссии по этике по мере необходимости.
</p>
<p>
	 4.2. Подготовка заседания Комиссии по этике, материалов на их рассмотрение, ведение делопроизводства и протоколов заседания Комиссии по этике возлагаются на ответственного секретаря.
</p>
<p>
	 Ответственный секретарь может быть назначен из числа лиц, не являющихся членами Комиссии по этике.
</p>
<p>
	 4.3. Заседание Комиссии по этике правомочно при участии в нем не менее половины ее членов.
</p>
<p>
	 4.4. Заседанием Комиссии по этике руководит председатель, а в его отсутствие - заместитель председателя либо член Комиссии по этике, избранный председательствующим на данном заседании.
</p>
<p>
	 4.5. Заседания могут проводиться с использованием средств видеоконференцсвязи.
</p>
<p>
	 4.6. Ход заседания Комиссии по этике может фиксироваться с использованием средств звуко - и видеозаписи.
</p>
<p>
	 4.7. Ход заседания Комиссии по этике фиксируется в кратком протоколе.
</p>
<p>
	 4.8. В кратком протоколе указываются:
</p>
<p>
	 дата и место проведения заседания;
</p>
<p>
	 время начала и окончания заседания;
</p>
<p>
	 фамилия, собственное имя, отчество присутствующих членов совета;
</p>
<p>
	 рассматриваемый на заседании вопрос;
</p>
<p>
	 сведения об использовании в ходе заседания средств звуко- или видеозаписи, систем видеоконференцсвязи и иных технических средств;
</p>
<p>
	 сведения о явке лиц, приглашенных на заседание;
</p>
<p>
	 сведения об устных заявлениях и ходатайствах участвующих в заседании лиц, о даче пояснений адвокатом, в отношении которого проводится проверка, об исследовании материалов;
</p>
<p>
	 дата составления краткого протокола.
</p>
<p>
	 4.9. Протокол подписывается председательствующим и ответственным секретарём Комиссии по этике.
</p>
<p>
	 4.10. В случае рассмотрения обращения, жалобы (представления, определения) в отношении члена Комиссии по этике адвокат, в отношении которого рассматривается жалоба (представление, определение), временно отстраняется от выполнения обязанностей члена Комиссии по этике.
</p>
<p>
	 4.11. За участие в работе Комиссии по этике ее члены получают вознаграждение в размере, определенном решением совета Белорусской республиканской коллегии адвокатов.
</p>
<p>
