</p>
<p align="center">
	 5. Решения Комиссии по этике
</p>
<p>
	 5.1. Решение Комиссии по этике по результатам рассмотрения обращений, жалоб (представлений, определений), мониторинга, обобщения практики принимается в форме заключения.
</p>
<p>
	 Решение Комиссии по этике по результатам рассмотрения обращений адвокатов, председателей, заместителей, советов территориальных коллегий адвокатов по вопросам применения Правил профессиональной этики адвоката и защиты прав адвокатов принимаются в форме разъяснений, рекомендаций.
</p>
<p>
	 Комиссия по этике принимает и иные решения по результатам рассмотрения вопросов, входящих в ее компетенцию.
</p>
<p>
	 Решение Комиссии по этике для органов адвокатского самоуправления носит рекомендательный характер.
</p>
<p>
	 5.2. Решения Комиссии по этике принимаются простым большинством голосов членов, участвующих в заседании. В случае равенства голосов председательствующий имеет право решающего голоса.
</p>
<p>
	 5.3. Голосование в ходе заседания Комиссии по этике является открытым.
</p>
<p>
	 Комиссия по этике вправе принять решение о проведении тайного голосования.
</p>
<p>
	 5.4. В решении указываются:
</p>
<p>
	 дата и место проведения заседания;
</p>
<p>
	 члены Комиссии по этике, участвовавшие в заседании;
</p>
<p>
	 сведения о явке лиц, приглашённых на заседание;
</p>
<p>
	 существенные обстоятельства, установленные при проведении мониторинга либо рассмотрении материалов проверки;
</p>
<p>
	 принятое решение.
</p>
<p>
	 Решения подписываются председательствующим и ответственным секретарём Комиссии по этике.
</p>
<p>
	 5.5. Копия решения Комиссии по этике по результатам проведенного мониторинга, а также о наличии в действиях адвоката признаков дисциплинарного проступка, Белорусской республиканской коллегией адвокатов направляется в соответствующую территориальную коллегию адвокатов для рассмотрения.
</p>
<p>
	 5.6. Решения Комиссии по этике хранятся в делах Белорусской республиканской коллегии адвокатов.
</p>
