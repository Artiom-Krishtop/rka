<p align="center">
	 Глава 1
</p>
<p align="center">
	 Общие положения
</p>
<p>
	 &nbsp;
</p>
<p>
	 В решении Конституционного Суда Республики Беларусь от 23 декабря 2011 г. «О соответствии Конституции Республики Беларусь Закона Республики Беларусь «Об адвокатуре и адвокатской деятельности в Республике Беларусь» отмечено, что на коллегии адвокатов, действующие на территории г. Минска и областей и предоставляющие в том числе определенный объем публично значимых услуг, государством возлагается конституционно-правовая задача оказания квалифицированной юридической помощи физическим и юридическим лицам.
</p>
<p>
	 Таким образом, выполнению адвокатами публичных функций корреспондирует создание нормативно-правовых и организационных механизмов, которые позволяют обеспечить осуществление адвокатской деятельности, направленной на реализацию конституционных положений, гарантирующих каждому право на юридическую помощь в целях осуществления и защиты прав и свобод (статья 62 Конституции Республики Беларусь).
</p>
<p>
	 Без обеспечения права на квалифицированную юридическую помощь невозможно достичь всеобщего уважения и соблюдения прав и свобод человека, предусмотренных Международными пактами «Об экономических, социальных и культурных правах» и «О гражданских и политических правах» (Нью-Йорк, 19 декабря 1966 г.), невозможно обеспечить необходимые гарантии для защиты человека, обвиняемого в совершении преступления, предусмотренные статьей7 Всеобщей декларации прав человека (принята на третьей сессии Генеральной Ассамблеи ООН 10 декабря 1948 г.).
</p>
<p>
	 Основные принципы, касающиеся роли юристов (принятые восьмым Конгрессом ООН по предупреждению преступности и обращению с правонарушителями, Гавана, 27 августа - 7 сентября 1990 г., далее – Основные принципы) предусматривают, что для обеспечения надлежащей защиты прав и основных свобод человека, пользоваться которыми должны все люди, независимо от того, являются ли эти права экономическими, социальными и культурными или гражданскими и политическими, необходимо, чтобы все люди действительно имели доступ к квалифицированной юридической помощи.Названный международный законодательный акт основополагающую роль в обеспечении предоставления квалифицированной юридической помощи всем нуждающимся отводит профессиональным ассоциациям адвокатов и обязывает их обеспечить надлежащую квалификацию и подготовку адвокатов и знание ими профессиональных идеалов и моральных обязанностей, а также прав человека и основных свобод, признанных национальным и международным правом (пункт 9).
</p>
<p>
	 Высокий уровень профессиональной компетенции является одним из основных принципов юридической профессии во всем мире. В Европе, в частности, этот принцип закреплен в Рекомендации № Rec(2000)21 Комитета министров Совета Европы «О свободе осуществления профессии адвоката», в которой предлагается государствам принимать все меры, которые они считают необходимыми для обеспечения высоких стандартов юридической подготовки. Обязательство адвокатов заниматься непрерывной профессиональной подготовкой существует в большинстве европейских стран, и, опираясь на принципы, определенные законодательством об адвокатуре, находит детальную проработку в документах, разрабатываемых адвокатским сообществом.
</p>
<p>
	 Основные принципы, касающиеся роли юристов, относят непрерывное образование и постоянное совершенствование знаний адвокатами к международно-признанным стандартам адвокатской профессии, несоблюдение которых является основанием дисциплинарной ответственности, и обязывают профессиональные ассоциации адвокатов способствовать непрерывному образованию и подготовке юристов (пункты 24 и 29).
</p>
<p>
	 Постоянное и непрерывное совершенствование знаний и повышение квалификации адвоката является обязательным стандартом адвокатской профессии по причине непрерывного развития теории права, изменения законодательства и практики его применения, совершенствования форм и методов адвокатской деятельности.
</p>
