<p align="center">
	 Глава 4. Делопроизводство по обращениям
</p>
<p>
	16. Делопроизводство по поступившим в адвокатское образование, в ТКА, БРКА обращениям, ведется отдельно от других видов делопроизводства в соответствии с Положением о порядке ведения делопроизводства по обращениям граждан и юридических лиц в государственных органах, иных организациях, у индивидуальных предпринимателей, утвержденным постановлением Совета Министров Республики Беларусь от 30 декабря 2011г. №1786.
</p>
<p>
	 17. В случае отзыва обращения его копия хранится вместе с заявлением об отзыве.
</p>
<p>
	 Таким же образом в делопроизводстве адвокатского образования, ТКА, БРКА должны храниться копии материалов, возвращенных заявителю в случае отказа в возбуждении дисциплинарного производства.
</p>
<p>
	 18. Прием и первичная обработка обращений осуществляется уполномоченным работником аппарата адвокатского образования, ТКА, БРКА.
</p>
<p>
	 Все обращения подлежат обязательной регистрации в день поступления в установленном порядке.
</p>
<p>
	 Обращения, поступившие почтой или с личного приема, в день регистрации и проставления на них штампа с указанием даты регистрации, передаются для рассмотрения руководителю адвокатского образования, председателям ТКА, БРКА (их заместителям).
</p>
<p>
	 19. Обращения одного и того же заявителя по одному и тому же вопросу, направленные различным адресатам и поступившие для рассмотрения ТК или БРКА, учитываются под регистрационным индексом первоначального обращения с добавлением дополнительного порядкового номера.
</p>
<p>
	 20. Ответ на обращение регистрируется в день его подписания руководителем адвокатского образования, органом управления ТКА, БРКА.
</p>
<p>
	 21. Обращения (в необходимых случаях и конверты к ним), копии ответов заявителям, документы и иные материалы, связанные с разрешением обращений, формируются в отдельные дела в соответствии с утвержденной номенклатурой дел в адвокатском образовании, ТКА, БРКА.
</p>
<p>
	 Документы в этих делах располагаются в хронологическом порядке. Каждое обращение и все документы по их рассмотрению и разрешению составляют в деле самостоятельную группу. Повторные обращения и дополнительные документы подшиваются к данной группе документов.
</p>
<p>
	 При формировании дел проверяется правильность направления документов в дело, их полнота (комплектность). Неразрешенные обращения, а также неправильно оформленные документы подшивать в дела запрещается.
</p>
<p>
	 22. Срок хранения письменных и (или) электронных обращений заявителей и документов, связанных с их рассмотрением, 5 лет (в случае неоднократного обращения - 5 лет с даты последнего обращения). По истечении срока хранения они подлежат уничтожению вместе с приложенными документами, о чем составляется акт.
</p>
<p>
	 23. По окончании года проводится анализ поступивших обращений и работы с ними, о чем составляется справка, копия которой направляется адвокатскими образованиями в ТКА к 15 января, а ТКА в БРКА к 1 февраля. БРКА готовит обобщенную справку к 15 февраля.
</p>
<p>
	 Результаты анализа могут быть использованы для совершенствования адвокатской деятельности, работы по рассмотрению и разрешению обращений и в иных целях.
</p>
<p>
	 Справка после рассмотрения её руководителем адвокатского образования, председателями ТКА и БРКА подшивается в делопроизводство по обращениям за истекший год.
</p>
