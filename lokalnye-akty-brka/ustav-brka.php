</p>
<p>
	 сокращенное на белорусском языке: БРКА.
</p>
<p>
	 4. Место нахождения Белорусской республиканской коллегии адвокатов: 220004, г. Минск, ул. Коллекторная, 10, каб.: 120,401,404 – 406.
</p>
 <b>
<h3>Глава 2&nbsp;</h3>
<h3>Цели и предмет деятельности Белорусской республиканской коллегии адвокатов</h3>
 </b>
<p>
	 5. Целями деятельности Белорусской республиканской коллегии адвокатов являются:
</p>
<p>
	 обеспечение организационных и правовых гарантий оказания квалифицированной юридической помощи физическим и юридическим лицам при осуществлении защиты их прав и законных интересов;
</p>
<p>
	 повышение роли адвокатуры в обществе и государстве;
</p>
<p>
	 объединение усилий адвокатов по совершенствованию правового государства;
</p>
<p>
	 повышение профессионального мастерства адвокатов;
</p>
<p>
	 обеспечение защиты профессиональных и социальных прав адвокатов, пропаганда здорового образа жизни среди адвокатов.
</p>
<p>
	 6. Предметом деятельности Белорусской республиканской коллегии адвокатов является:
</p>
<p>
	 представление и защита интересов адвокатов во взаимоотношениях с государственными органами и иными организациями;
</p>
<p>
	 координация деятельности территориальных (Минской городской и областных) коллегий адвокатов (далее – территориальные коллегии адвокатов);
</p>
<p>
	 осуществление мер, направленных на повышение доступности и качества оказания юридической помощи;
</p>
<p>
	 определение порядка кадрового обеспечения территориальных коллегий адвокатов по согласованию с Министерством юстиции;
</p>
<p>
	 осуществление мониторинга деятельности адвокатов;
</p>
<p>
	 осуществление организационных, распорядительных и контрольных функций при проверке соблюдения нормативных правовых и локальных актов органами управления территориальных коллегий адвокатов и адвокатами;
</p>
<p>
	 изучение и обобщение деятельности территориальных коллегий адвокатов и адвокатов, выработка рекомендаций по ее совершенствованию;
</p>
<p>
	 организация учебы адвокатов в целях повышения профессионального мастерства;
</p>
<p>
	 участие совместно с органами управления территориальных коллегий адвокатов в разработке и реализации программ по созданию материально-технической, правовой и информационной базы адвокатуры, содействие внедрению современных средств оргтехники и связи в работу органов управления территориальных коллегий адвокатов, юридических консультаций;
</p>
<p>
	 организация конференций, конгрессов, семинаров, совещаний по вопросам адвокатской деятельности, сотрудничество с государственными органами и иными организациями, деятельность которых связана с деятельностью адвокатуры;
</p>
<p>
	 участие в работе по совершенствованию законодательства, регулирующего деятельность адвокатуры, и содействие научным разработкам, регулирующим эту деятельность;
</p>
<p>
	 издание учебной и методической литературы и распространение информации о деятельности адвокатуры;
</p>
<p>
	 прогнозирование потребности республики в адвокатах;
</p>
<p>
	 участие в законотворческой деятельности и правовом воспитании граждан;
</p>
<p>
	 установление и развитие деловых связей с адвокатами и адвокатскими формированиями других государств.
</p>
