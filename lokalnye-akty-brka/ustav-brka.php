<h3>Глава 1&nbsp;</h3>
<h3>Общие положения&nbsp;</h3>
<p>
</p>
<p>
	 1. Белорусская республиканская коллегия адвокатов является органом адвокатского самоуправления.
</p>
<p>
	 Белорусская республиканская коллегия адвокатов зарегистрирована Министерством юстиции в Едином государственном регистре юридических лиц и индивидуальных предпринимателей 28 ноября 2002 года за № 101304381.
</p>
<p>
	 2. Белорусская республиканская коллегия адвокатов в своей деятельности руководствуется Конституцией Республики Беларусь, Законом Республики Беларусь от 30 декабря 2011 г. «Об адвокатуре и адвокатской деятельности в Республике Беларусь» с изменениями и дополнениями, внесенными Законом Республики Беларусь от 27 мая 2021 г. №113-З (далее – Закон), декретами и указами Президента Республики Беларусь, издаваемыми в соответствии с ними нормативными правовыми актами и настоящим Уставом.
</p>
<p>
	 3. Белорусская республиканская коллегия адвокатов является некоммерческой организацией, основанной на обязательном членстве областных, Минской городской коллегий адвокатов, имеет самостоятельный баланс, счета в банке и (или) небанковских кредитно-финансовых организациях, в собственности обособленное имущество, несет самостоятельную ответственность по своим обязательствам и приобретает права юридического лица с момента государственной регистрации в Министерстве юстиции.
</p>
<p>
	 Белорусская республиканская коллегия адвокатов является юридическим лицом, имеет печать, штампы и бланки с адресом и своим наименованием, а также символику, которая подлежит государственной регистрации в порядке, установленном законодательством.
</p>
<p>
	 Белорусская республиканская коллегия адвокатов не является субъектом хозяйствования.
</p>
<p>
	 Наименование коллегии адвокатов:
</p>
<p>
	 полное на русском языке: Белорусская республиканская коллегия адвокатов;
</p>
<p>
	 полное на белорусском языке: Беларуская рэспубліканская калегія адвакатаў;
