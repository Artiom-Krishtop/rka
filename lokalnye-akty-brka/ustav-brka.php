<h3>Глава 3 <br>
 Права и обязанности Белорусской республиканской коллегии адвокатов</h3>
<p>
	 7. Белорусская республиканская коллегия адвокатов:
</p>
<p>
	 разрабатывает с участием территориальных коллегий адвокатов и принимает локальные нормативные правовые акты, регулирующие адвокатскую деятельность;
</p>
<p>
	 проводит проверки работы территориальных коллегий адвокатов, в том числе их финансовой деятельности;
</p>
<p>
	 устанавливает порядок направления Белорусской республиканской коллегией адвокатов претендентов на стажировку;
</p>
<p>
	 определяет условия и размер оплаты труда адвокатов при оказании юридической помощи за счет средств территориальных коллегий адвокатов, а также оплаты труда стажеров адвоката;
</p>
<p>
	 рассматривает жалобы на решения о привлечении адвоката к дисциплинарной ответственности либо о прекращении дисциплинарного производства в отношении адвоката;
</p>
<p>
	 рассматривает жалобы (представления, определения) на действия председателей коллегий адвокатов, их заместителей, членов советов коллегий адвокатов;
</p>
<p>
	 приостанавливает не соответствующие законодательству, решениям совета Белорусской республиканской коллегии адвокатов, Уставу либо принятые с нарушением установленного порядка решения органов управления территориальных коллегий адвокатов, и вносит в эти органы представления об отмене таких решений, а также обращается в суд с заявлениями об отмене этих решений в случае необоснованного отказа в удовлетворении соответствующих представлений;
</p>
<p>
	 принимает в пределах своих полномочий решения по улучшению работы советов территориальных коллегий адвокатов, юридических консультаций;
</p>
<p>
	 представляет интересы территориальных коллегий адвокатов и адвокатов в государственных органах и иных организациях, направляет предложения по разработке и принятию нормативных правовых актов или вносит проекты нормативных правовых актов государственным органам (должностным лицам), имеющим соответствующие полномочия;
</p>
<p>
	 устанавливает порядок и сроки представления годовой отчетности территориальными коллегиями адвокатов;
</p>
<p>
	 устанавливает порядок и сроки принесения присяги адвоката, а также порядок её хранения;
</p>
<p>
	 принимает рекомендации по повышению квалификации адвокатов;
</p>
<p>
	 вносит предложения по вопросам деятельности адвокатуры в республиканские органы управления и органы местного управления и самоуправления.
</p>
<h3>
<p>
	 Глава 4
</p>
 Органы управления Белорусской республиканской коллегии адвокатов</h3>
<h3></h3>
<p>
	 8. Совет Белорусской республиканской коллегии адвокатов является коллегиальным исполнительным органом управления, формируется из представителей, избираемых на общих собраниях (конференциях) членов территориальных коллегий адвокатов – по два представителя от каждой территориальной коллегии адвокатов сроком на четыре года. Председатели территориальных коллегий адвокатов являются членами совета Белорусской республиканской коллегии адвокатов по должности.
</p>
<p>
	 Порядок избрания и отзыва представителей территориальных коллегий адвокатов определяется их уставами.
</p>
<p>
	 В случае неисполнения отдельными членами совета Белорусской республиканской коллегии адвокатов, в том числе председателем и заместителем председателя, требований Закона, настоящего Устава, совет принимает решение о прекращении полномочий председателя, заместителя председателя, члена совета Белорусской республиканской коллегии адвокатов.
</p>
<p>
	 9. Полномочия совета могут быть прекращены до истечения срока по решению съезда адвокатов за неисполнение советом требований, предусмотренных Законом, настоящим Уставом.
</p>
<p>
	 10. Заседания совета Белорусской республиканской коллегии адвокатов созываются председателем Белорусской республиканской коллегии адвокатов и проводятся не реже одного раза в три месяца.
</p>
<p>
	 Заседание совета Белорусской республиканской коллегии адвокатов может быть созвано по инициативе более половины членов совета Белорусской республиканской коллегии адвокатов.
</p>
<p>
	 Заседание совета Белорусской республиканской коллегии адвокатов правомочно, если на нем присутствуют не менее 2/3 его членов.
</p>
<p>
	 Решения совета принимаются открытым или тайным голосованием в форме постановления.
</p>
<p>
	 Решение совета считается принятым, если за него проголосовало более половины голосов членов совета, участвующих в заседании.
</p>
<p>
	 В случае равенства голосов членов совета при принятии решения председательствующий на совете имеет право решающего голоса.
</p>
<p>
	 Решения совета Белорусской республиканской коллегии адвокатов, принятые в пределах ее компетенции, обязательны для исполнения всеми адвокатами.
</p>
<p>
	 11. К компетенции совета Белорусской республиканской коллегии адвокатов относятся:
</p>
<p>
	 представление интересов адвокатов в государственных органах и иных организациях;
</p>
<p>
	 координация деятельности территориальных коллегий адвокатов;
</p>
<p>
	 осуществление информационного и методического обеспечения территориальных коллегий адвокатов;
</p>
<p>
	 утверждение Устава Белорусской республиканской коллегии адвокатов, вносимых в него изменений и дополнений;
</p>
<p>
	 утверждение Примерного устава территориальной коллегии адвокатов, вносимых в него изменений и дополнений;
</p>
<p>
	 избрание председателя Белорусской республиканской коллегии адвокатов и его заместителя;
</p>
<p>
	 рассмотрение представлений Министерства юстиции о досрочном отзыве председателя Белорусской республиканской коллегии адвокатов, допустившего нарушение законодательства, настоящего Устава;
</p>
<p>
	 участие в разработке Правил профессиональной этики адвоката, утверждение положения о ревизионной комиссии Белорусской республиканской коллегии адвокатов, методических рекомендаций и других актов, регулирующих вопросы осуществления адвокатской деятельности;
</p>
<p>
	 утверждение годовой сметы доходов и расходов Белорусской республиканской коллегии адвокатов, фонда развития адвокатуры, порядка и размеров выплат вознаграждения председателю, его заместителю и членам совета Белорусской республиканской коллегии адвокатов, председателю и членам ревизионной комиссии Белорусской республиканской коллегии адвокатов, утверждение штатного расписания Белорусской республиканской коллегии адвокатов;
</p>
<p>
	 заслушивание отчетов председателя Белорусской республиканской коллегии адвокатов, председателя ревизионной комиссии Белорусской республиканской коллегии адвокатов;
</p>
<p>
	 определение нормы представительства от территориальных коллегий адвокатов на съезде адвокатов, образование организационного комитета для подготовки и проведения съезда адвокатов;
</p>
<p>
	 разработка и внесение в установленном порядке предложений о совершенствовании законодательства;
</p>
<p>
	 определение размера отчислений территориальных коллегий адвокатов на содержание Белорусской республиканской коллегии адвокатов, исходя из затрат, необходимых для ее содержания и выполнения возложенных на нее функций, численности адвокатов в территориальных коллегиях адвокатов, и в фонд развития адвокатуры;
</p>
<p>
	 принятие решений о создании фонда развития адвокатуры, порядке формирования и расходования средств этого фонда, направлениях и целях их использования по согласованию с Министерством юстиции с учетом предложений территориальных коллегий адвокатов;
</p>
<p>
	 определение порядка рассмотрения обращений граждан, в том числе индивидуальных предпринимателей, и юридических лиц в территориальных коллегиях адвокатов;
</p>
<p>
	 определение порядка ведения дисциплинарного производства в отношении адвокатов;
</p>
<p>
	 возбуждение дисциплинарного производства в отношении председателей и заместителей председателей коллегий адвокатов и рассмотрение дел о дисциплинарной ответственности в отношении председателей и членов советов коллегий адвокатов;
</p>
<p>
	 рассмотрение жалоб на решения о привлечении адвоката к дисциплинарной ответственности либо о прекращении дисциплинарного производства в отношении адвоката;
</p>
<p>
	 обеспечение соответствия оплаты юридической помощи ее объему, сложности и качеству;
</p>
<p>
	 приостановление не соответствующих законодательству, решениям совета Белорусской республиканской коллегии адвокатов, Уставу либо принятых с нарушением установленного порядка решений органов управления территориальных коллегий адвокатов и внесение в эти органы представлений об отмене таких решений, а также обращение в суд с заявлениями об отмене этих решений в случае необоснованного отказа в удовлетворении соответствующих представлений;
