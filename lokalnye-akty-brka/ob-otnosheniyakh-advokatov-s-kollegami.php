<p align="center">
	 ПОСТАНОВЛЯЕТ:
</p>
<p align="center">
	 &nbsp;
</p>
<p>
	 1. Адвокаты обязаны исполнять свои обязанности по защите интересов подозреваемых, обвиняемых квалифицированно и добросовестно вне зависимости от основания участия по уголовному делу (по приглашению либо по назначению).
</p>
<p>
	 2.Адвокат при заключении договора на оказание юридической помощи по уголовному делу, по которому ранее участвовал другой адвокат, обязан незамедлительно проинформировать об этом коллегу.
</p>
<p>
	 3. Адвокат до заключения договора на оказание юридической помощи по уголовным делам обязан разъяснить клиенту необходимость уплаты гонорара адвокату, который ранее участвовал по данному делу в порядке статьи 46 УПК, за исключением случаев участия при даче бесплатной консультации подозреваемому, обвиняемому до первого допроса.
</p>
<p>
	 4. Адвокат, участвующий по уголовному делу на основании договора на оказание юридической помощи, должен проконтролировать взыскание в пользу финансовых органов расходов по оплате юридической помощи за участие адвоката в порядке статьи 46 УПК.
</p>
<p>
	 5. По каждому случаю неисполнения адвокатами пунктов 1-3 выяснять причины и в зависимости от конкретных обстоятельств решать вопросы о привлечении адвоката к дисциплинарной ответственности.
</p>
<p>
	 7. Контроль за исполнением данного постановления возложить на советы территориальных коллегий адвокатов.
</p>
