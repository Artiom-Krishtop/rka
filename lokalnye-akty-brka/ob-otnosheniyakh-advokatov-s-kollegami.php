<p>
	 Об отношениях адвокатов с коллегами
</p>
<p>
	 В соответствии с пунктами 19 и 20 Правил профессиональной этики адвоката адвокат строит свои отношения с другими адвокатами на основе взаимного уважения и соблюдения их профессиональных прав. Адвокат должен быть отзывчивым и доброжелательным, уважать честь и достоинство своих коллег.
</p>
<p>
	 В адрес Белорусской республиканской коллегии адвокатов поступает информация от адвокатов, в которой обращается внимание на нарушения адвокатами вышеуказанных норм, а именно, на неправильное поведение адвокатов, вступающих в уголовный процесс на основании договора на оказание юридической помощи, по отношению к адвокатам, которые осуществляли защиту по этому же уголовному делу по требованию органа, ведущего уголовный процесс:
</p>
<p>
	 не информируют заблаговременно коллегу о заключении договора,
</p>
<p>
	 не разъясняют подзащитному либо лицу, заключившему договор в интересах клиента, о необходимости внесения оплаты за оказанную юридическую помощь коллеге,
</p>
<p>
	 не взыскивают с обвиняемого в пользу финансовых органов расходы по оплате юридической помощи, оказанной коллегой в порядке статьи 46 УПК.
</p>
<p>
	 Вследствие таких действий нарушаются интересы не только адвокатов, участвовавших по делу в порядке статьи 46 УПК, но и государства.
</p>
<p>
	 Имеют место случаи, когда граждане категорически отказываются заключать договор с адвокатом, участвовавшим в порядке статьи 46 УПК, мотивируя это тем, что адвокат формально отнесся к выполнению своих обязанностей.
</p>
<p>
	 На основании изложенного Совет Белорусской республиканской коллегии адвокатов
</p>
