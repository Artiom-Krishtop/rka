<p align="center">
	 4. Методика организации устной пропаганды правовых знаний
</p>
<p>
	Основной формой устной пропаганды является проведение мероприятий, тематика которых определяется с учетом злободневности вопросов, предложенных самими гражданами, юридическими лицами и индивидуальными предпринимателями.
</p>
<p>
	 Юридическая консультация соответствующей административно-территориальной или территориальной единицы размещает в электронных и печатных средствах массовой информации, в эфирном и кабельном вещании теле- и радиоканалов, созданных с государственным участием, информацию о сообщении в их адрес проблем, исходя из примененного законодательства, совершенных правонарушений и судебной практики.
</p>
<p>
	 Полученные сообщения анализируются, и на основании анализа готовится примерная тематика семинаров на месяц, квартал и год.
</p>
<p>
	 Мероприятия проводятся адвокатами адвокатских образований соответствующей административно-территориальной или территориальной единицы поочередно исходя из принципа справедливости не реже:
</p>
<p>
	 1 раза в два месяца – на уровне районов;
</p>
<p>
	 1 раза в четыре месяца – на уровне областей;
</p>
<p>
	 1 раза в год – на уровне республики по наиболее важным вопросам.
</p>
