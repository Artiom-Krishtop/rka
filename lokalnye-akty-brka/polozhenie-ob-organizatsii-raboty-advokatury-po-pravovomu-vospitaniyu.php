<p align="center">
	 5. Меры финансового и организационного характера
</p>
<p>
	Территориальные коллегии адвокатов и Белорусская республиканская коллегия адвокатов в пределах своей компетенции участвуют в осуществлении мероприятий, проводимых по разъяснению законодательства и правовому воспитанию, используя средства территориальных коллегий адвокатов, Белорусской республиканской коллегии адвокатов, фонд развития адвокатуры, а также создают систему моральных и материальных стимулов и поощрений активного участия в их осуществлении.
</p>
<p>
	 Для финансирования разработки информационных, методических материалов и осуществления иных мероприятий в сфере развития правовой грамотности и правосознания граждан могут использоваться иные источники, не запрещенные законодательством.
</p>
<p>
	 Организация и координация работы по взаимодействию с государственными органами и общественностью, разъяснению законодательства и правовому просвещению возлагается:
</p>
<p>
	 в Белорусской республиканской коллегии адвокатов - на заместителя председателя Белорусской республиканской коллегии адвокатов;
</p>
<p>
	 в территориальных коллегиях адвокатов – на заместителей председателей территориальных коллегий адвокатов;
</p>
<p>
	 в адвокатских образованиях - соответственно на заведующих юридическими консультациями, управляющих партнеров, адвокатов, осуществляющих адвокатскую деятельность индивидуально.
</p>
<p>
	 Подготовка и участие в семинарах устной пропаганды по решению совета территориальной коллегии адвокатов может засчитываться в качестве учебы адвокатов по совершенствованию своих знаний и повышения квалификации.
