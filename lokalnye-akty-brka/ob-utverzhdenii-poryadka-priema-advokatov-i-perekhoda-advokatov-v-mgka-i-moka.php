</p>
<p>
	 ПОСТАНОВЛЯЕТ:
</p>
<p>
	 1. Требование статьи 11 Закона Республики Беларусь «Об адвокатуре и адвокатской деятельности в Республике Беларусь» об обязанности территориальной коллегии принять адвоката применяется к адвокатам, начинающим осуществление адвокатской деятельности после прохождения стажировки в соответствующей территориальной коллегии и получения лицензии.
</p>
<p>
	 2. Решение о переходе адвоката из областной в Минскую городскую коллегию адвокатов принимается на заседании совета Белорусской республиканской коллегии адвокатов при наличии письменного согласия председателей соответствующей областной и Минской городской коллегий адвокатов.
</p>
<p>
	 3. В порядке, предусмотренном пунктом 2 постановления, принимается решение о переходе адвоката из другой областной в Минскую областную коллегию адвокатов с местом работы в юридической консультации, расположенной на территории г.Минска.
</p>
<p>
	 4. Утвердить образец заявления адвоката о переходе в Минскую городскую (областную) коллегию адвокатов (прилагается).
</p>
<p>
	 5. Адвокат, желающий перейти в Минскую городскую (областную) коллегию адвокатов, составляет заявление в двух экземплярах.
</p>
<p>
	 После согласования перехода председателями коллегий один экземпляр заявления хранится в делах областной коллегии, второй – в делах Минской городской (областной) коллегии.
</p>
<p>
	 Прилагаемые к заявлению документы хранятся в делах областной коллегии, из которой адвокат желает перейти.
</p>
<p>
	 5. Признать утратившим силу постановление совета Республиканской коллегии адвокатов от 22.08.2016 г. №02/05 «О применении статьи 11 Закона республики Беларусь «Об адвокатуре и адвокатской деятельности в Республике Беларусь».
