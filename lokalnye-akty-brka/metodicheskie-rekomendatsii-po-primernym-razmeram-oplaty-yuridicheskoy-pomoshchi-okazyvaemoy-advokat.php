</p>
<p>
	 5. Оплата труда адвоката за счет средств территориальной коллегии адвокатов
</p>
<p>
	 5.1. Основания оказания юридической помощи за счет средств коллегий адвокатов установлены пунктом 1 статьи 28 Закона.
</p>
<p>
	 Адвокатам следует получать от граждан сведения и документы, подтверждающие право на получение юридической помощи за счет средств коллегии.
</p>
<p>
	 5.2. В случае, если при получении юридической помощи, которая может быть оказана за счет средств коллегии, гражданин желает произвести оплату, с ним заключается договор об оказании юридической помощи. Перед заключением договора адвокат разъясняет гражданину право на получение юридической помощи за счет средств коллегии в установленном порядке. В договоре производится запись о том, что гражданину разъяснено право на получение юридической помощи за счет средств коллегии адвокатов, которая удостоверяется подписью гражданина.
</p>
<p>
	 5.3. Оказание юридической помощи за счет средств коллегии лицам, не указанным в статье 28 Закона, возможно только по решению соответствующей территориальной коллегии адвокатов. Адвокат не наделен правом самостоятельно устанавливать категории граждан, которым он желает оказать юридическую помощь без взимания гонорара (бесплатно).
</p>
