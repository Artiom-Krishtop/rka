<p>
	 4. Оплата труда адвоката за счет средств местного бюджета
</p>
<p>
	 4.1. Размер оплаты труда адвоката, участвующего в качестве защитника в уголовном судопроизводстве по назначению через территориальную коллегию адвокатов по требованию органа, ведущего уголовный процесс, установлен постановлением Совета Министров Республики Беларусь от 8 августа 2007 г. № 1003 «О порядке возмещения расходов по оплате труда адвокатов за счет средств местного бюджета» и Инструкцией о порядке возмещения расходов по оплате труда адвокатов за счет средств местного бюджета, утвержденной постановлением Министерства юстиции Республики Беларусь и Министерства финансов Республики Беларусь от 30 августа 2007 г. № 57/129.
</p>
<p>
	 4.2. Юридическая помощь жертвам торговли людьми, а также лицам, пострадавшим в результате акта терроризма, включая разъяснение их прав и обязанностей, предусмотренных законодательством Республики Беларусь, оказывается за счет средств республиканского бюджета в порядке, установленном постановлением Совета Министров Республики Беларусь от 6 февраля 2012 г. № 122 «О порядке возмещения расходов на оплату труда адвокатов по оказанию юридической помощи жертвам торговли людьми, лицам, пострадавшим в результате акта терроризма».
</p>
