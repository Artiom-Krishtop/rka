<p>
	 2. Критерии определения размера оплаты юридической помощи
</p>
<p>
	 2.1. При определении размера оплаты юридической помощи (гонорара) надлежит принимать во внимание факторы, указанные в пункте 54 Правил профессиональной этики адвоката, утвержденных постановлением Министерства юстиции Республики Беларусь от 30.09.2021 N 180:
</p>
<p>
	 опыт и квалификацию адвоката, требуемые для оказания юридической помощи надлежащим образом;
</p>
<p>
	 профессиональный авторитет адвоката;
</p>
<p>
	 новизну и сложность вопросов;
</p>
<p>
	 необходимые затраты времени и труда;
</p>
<p>
	 вероятность того, что принятие данного поручения ограничит возможности адвоката одновременно работать по другим делам;
</p>
<p>
	 сроки, установленные клиентом или обстоятельствами;
</p>
<p>
	 необходимость привлечения специалистов;
</p>
<p>
	 расходы, связанные с оказанием юридической помощи;
</p>
<p>
	 практику определения гонорара в данном регионе, а также в регионе, в котором клиент имеет постоянное местопребывание;
</p>
<p>
	 иные факторы.
</p>
<p>
	 2.2. При определении сложности поручения по ведению уголовных дел следует учитывать следующие факторы:
</p>
<p>
	 сложность и объем дела;
</p>
<p>
	 количество подозреваемых, обвиняемых, потерпевших в уголовном деле;
</p>
<p>
	 нахождение подозреваемого, обвиняемого под стражей;
</p>
<p>
	 обвинение подзащитного в нескольких составах преступлений, в совершении тяжкого и (или) особо тяжкого преступления;
</p>
<p>
	 позицию подозреваемого, обвиняемого;
</p>
<p>
	 наличие заключений экспертиз, актов проверок;
</p>
<p>
	 необходимость собирания сведений для защиты прав подозреваемого, обвиняемого и оказания им юридической помощи путем опроса физических лиц, направлении запросов, предоставлении справок, характеристик и иных документов, необходимость запрашивать с согласия подозреваемого, обвиняемого мнения специалистов для разъяснения возникающих в связи с осуществлением защиты вопросов, требующих специальных знаний;
</p>
<p>
	 рассмотрение дела по первой инстанции вышестоящими судами;
</p>
<p>
	 неоднократность его рассмотрения в судебных инстанциях либо рассмотрение дела после отмены постановления суда в апелляционном, надзорном порядке.
</p>
<p>
	 2.3. При определении сложности поручения по ведению гражданских, экономических дел следует учитывать следующие факторы:
</p>
<p>
	 сложность и объем дела;
</p>
<p>
	 позицию лица, обратившегося за оказанием юридической помощи;
</p>
<p>
	 количество истцов или ответчиков, третьих лиц;
</p>
<p>
	 наличие нескольких исковых требований, встречного иска;
</p>
<p>
	 наличие заключений экспертиз, актов проверок;
</p>
<p>
	 наличие иностранных и международных элементов (нерезидентов, законодательства и практики других стран, международных соглашений и т.д.);
</p>
<p>
	 необходимость собирания доказательств по делу, производства сложных расчетов, изучения дополнительных материалов, в том числе уголовных, гражданских и административных дел, материалов об отказе в возбуждении уголовного дела, имеющих отношение к рассматриваемому делу, а также изучения специальной литературы в связи с возникающими по делу вопросами;
</p>
<p>
	 рассмотрение дела по первой инстанции вышестоящими судами;
</p>
<p>
	 неоднократность рассмотрения дела в судебных инстанциях либо рассмотрение дела после отмены постановления суда в апелляционном и надзорном порядке.
</p>
<p>
	 2.4. При определении сложности поручения по ведению административных дел следует учитывать следующие факторы:
</p>
<p>
	 количество вменяемых административных правонарушений;
</p>
<p>
	 отношение к вменяемым административным правонарушениям лица, в отношении которого ведется административный процесс;
</p>
<p>
	 необходимость посещения административно задержанных и лиц, на которых наложено административное взыскание в виде административного ареста;
</p>
<p>
	 наличие заключений экспертиз, актов проверок;
</p>
<p>
	 необходимость собирания дополнительных сведений, касающихся совершенного административного правонарушения, характеристик и иных документов, а также изучения специальной литературы в связи с возникающими по делу вопросами.
</p>
