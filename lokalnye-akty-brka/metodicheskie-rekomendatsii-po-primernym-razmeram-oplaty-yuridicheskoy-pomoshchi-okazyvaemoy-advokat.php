<p>
	 1. Общие положения
</p>
<p>
	 1.1. Принятие настоящих Методических рекомендаций по установлению размеров оплаты юридической помощи, оказываемой адвокатами гражданам, относится к компетенции совета Белорусской республиканской коллегии адвокатов в части реализации полномочий по координации деятельности территориальных коллегий адвокатов, осуществлению мер, направленных на повышение доступности оказания юридической помощи и по обеспечению принципов организации адвокатуры и адвокатской деятельности - соответствия оплаты юридической помощи её объему, сложности и качеству, реализации гарантированного Конституцией Республики Беларусь права граждан на юридическую помощь.
</p>
<p>
	 Действие Методических рекомендаций распространяется на всех адвокатов Республики Беларусь.
</p>
<p>
	 1.2. Основанием оплаты труда адвокатов за юридическую помощь, оказываемую ими гражданам, согласно статье 27 Закона Республики Беларусь «Об адвокатуре и адвокатской деятельности в Республике Беларусь» от 30.12.2011 (далее – Закон), является договор между адвокатом и клиентом или иным лицом, действующим в интересах клиента. Существенными условиями <a href="consultantplus://offline/ref=6500A51784E76EB710E5229F02ED0459F08B48C7AC9AFB601E8575324AC67A68A71C43E014B5425E5F617DBD724514109E9991CFEB1E0CF4381A3725EEM5s6L">договора</a> на оказание юридической помощи являются вид (виды) юридической помощи и порядок и размер её оплаты.
</p>
