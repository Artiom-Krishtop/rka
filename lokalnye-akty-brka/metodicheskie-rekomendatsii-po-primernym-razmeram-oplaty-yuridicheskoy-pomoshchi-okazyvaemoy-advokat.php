<p>
	 3. Размеры гонорара
</p>
<p>
	 3.1. Размеры гонорара и порядок его определения формируются по договоренности между адвокатом и клиентом и указываются в договоре об оказании юридической помощи.
</p>
<p>
	 Гонорар может определяться в фиксированной сумме, в оплате за день участия адвоката в оказании юридической помощи, в оплате согласованной почасовой ставки, с доплатой за позитивный результат по делу и т.д.
</p>
<p>
	 3.2. Консультация, совет, не требующие ознакомления с документами – от двух до пяти базовых величин.
</p>
<p>
	 3.3. Консультация, совет, требующие ознакомления с документами либо представляющие сложность – от трех до десяти базовых величин.
</p>
<p>
	 3.4. Составление заявлений и других документов правового характера – от трех до пятнадцати базовых величин.
</p>
<p>
	 3.5. Составление жалоб на постановления по делам об административных правонарушениях, апелляционных и кассационных жалоб на решения и приговоры судов, жалоб в порядке надзора по гражданским, экономическим и уголовным делам, и иных сложных правовых документов, в том числе связанных с истребованием адвокатом дополнительных материалов и (или) ознакомлением с дополнительными материалами, – от пяти до двадцати базовых величин.
</p>
<p>
	 При этом повторное составление адвокатом, составлявшим первичную надзорную жалобу, жалоб в порядке надзора по гражданским, экономическим и уголовным делам, – от трех до восьми базовых величин, но не более 50% размера гонорара, оплаченного за составление первичной жалобы в порядке надзора;
</p>
<p>
	 3.6. Один день участия адвоката:
</p>
<p>
	 по уголовным делам:
</p>
<p>
	 в производстве по материалам и уголовному делу – от восьми до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде первой инстанции – от восьми до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде второй инстанции – от пяти до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде в порядке надзора – от пяти до пятнадцати базовых величин;
</p>
<p>
	 посещение в местах лишения свободы* – от пяти до пятнадцати базовых величин;
</p>
<p>
	 по гражданским делам:
</p>
<p>
	 подготовка и участие в суде первой инстанции – от восьми до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде второй инстанции – от восьми до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде в порядке надзора – от восьми до пятнадцати базовых величин;
</p>
<p>
	 по экономическим делам:
</p>
<p>
	 подготовка и участие в суде первой инстанции – от десяти до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде второй инстанции – от десяти до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде кассационной инстанции – от десяти до пятнадцати базовых величин;
</p>
<p>
	 подготовка и участие в суде в порядке надзора – от десяти до пятнадцати базовых величин;
</p>
<p>
	 по административным делам:
</p>
<p>
	 подготовка и участие в административном процессе – от пяти до пятнадцати базовых величин;
</p>
<p>
	 в ознакомлении с материалами:
</p>
<p>
	 уголовных дел – от восьми до пятнадцати базовых величин;
</p>
<p>
	 гражданских, экономических дел – от восьми до пятнадцати базовых величин;
</p>
<p>
	 административных дел – от пяти до пятнадцати;
</p>
<p>
	 при оказании иной юридической помощи (участие в переговорах, подача адвокатского запроса и получение ответа на запрос, представление интересов в государственных и иных органах и организациях и т.д.) – от пяти до двадцати базовых величин.
</p>
<p>
	 3.7. При сложности дела, а также по делам, связанным с выездом адвоката в командировку, иной от расположения юридической консультации, в которой работает адвокат, населенных пункт – от десяти до двадцати базовых величин за один день участия.
</p>
<p>
	 3.8. Размер гонорара в фиксированной сумме определяется по договоренности между адвокатом и клиентом исходя из сложности и продолжительности выполнения поручения.
</p>
<p>
	 3.9. Размер гонорара, определенный почасовой ставкой, определяется по договоренности между адвокатом и клиентом исходя из сложности и продолжительности выполнения поручения.
</p>
<p>
	 При этом гонорар, определенный почасовой ставкой, за поручение, выполняемое в течение дня, не может превышать размеров гонорара адвоката, установленных пунктами 3.6, 3.7 настоящих Методических рекомендаций.
</p>
<p>
	 3.10. Доплата за позитивный результат по делу определяется по договоренности между адвокатом и клиентом.
</p>
<p>
	 3.11. Адвокат не должен принимать на себя обязательство об оказании юридической помощи с условием выплаты гонорара в зависимости от будущих результатов.
</p>
<p>
	 3.12. При заключении договора об оказании юридической помощи с гражданином размер оплаты юридической помощи может быть определен адвокатом ниже минимальных размеров, предусмотренных пунктами 3.2 - 3.7 настоящих Методических рекомендаций, с учетом имущественного и социального положения лица, обратившегося за оказанием юридической помощи (доход на члена семьи ниже прожиточного минимума в среднем на душу населения; инвалидность, получение социальной пенсии и др.).
</p>
<p>
	 3.13. При заключении договора об оказании юридической помощи с гражданином размер оплаты юридической помощи может быть определен выше максимальных размеров оплаты, предусмотренных пунктами 3.2 - 3.7, 3.9 настоящих Методических рекомендаций. В этом случае адвокат должен обосновать определение повышенного размера гонорара.
</p>
<p>
	 3.14. Заведующий юридической консультацией осуществляет постоянный контроль за соблюдением адвокатами Правил профессиональной этики адвокатов при определении размера гонорара.
</p>
<p>
	 При выявлении обстоятельств, свидетельствующих о несоответствии оплаты юридической помощи ее объему, сложности и качеству заведующий юридической консультацией не утверждает отчет об оказанной юридической помощи в указанной части и передает необходимые документы в совет территориальной коллегии адвокатов, который решает вопрос о наличии в действиях адвоката нарушений Правил профессиональной этики адвоката.
</p>
<p>
	 3.15. Размеры оплаты юридической помощи, определенные настоящими рекомендациями, не распространяются на:
</p>
<p>
	 нерезидентов Республики Беларусь;
</p>
<p>
	 граждан Республики Беларусь, постоянно проживающих за пределами Республики Беларусь;
</p>
<p>
	 граждан Республики Беларусь по вопросам наследства, открывшегося за границей, включая розыск наследников и генеалогические исследования;
</p>
<p>
	 граждан Республики Беларусь, являющихся учредителями (участниками, собственниками, акционерами) юридических лиц Республики Беларусь по вопросам учредительства (участия, собственности, владения акциями) в них, осуществления предпринимательской и иной хозяйственной деятельности;
</p>
<p>
	 граждан Республики Беларусь, осуществляющих предпринимательскую деятельность без образования юридического лица в качестве индивидуальных предпринимателей, по вопросам, связанным с осуществлением ими предпринимательской и иной хозяйственной деятельности.
</p>
