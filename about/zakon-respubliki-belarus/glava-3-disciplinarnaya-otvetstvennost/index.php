	</p>
	<p>
		 совершения проступков, не совместимых со званием адвоката, перечень которых установлен Правилами профессиональной этики адвоката;
	</p>
	<p>
		 нарушения порядка возмещения расходов на оплату труда адвоката в случае оказания юридической помощи по требованию органа, ведущего уголовный процесс, повлекшего незаконное расходование средств бюджета на сумму, в десять и более раз превышающую размер базовой величины, установленный на день совершения нарушения;
	</p>
	<p>
