	</p>
	<p>
		 выговор;
	</p>
	<p>
		 исключение из территориальной коллегии адвокатов.
	</p>
	<p>
		<b>Статья 22. Порядок применения дисциплинарных взысканий</b><br>
	</p>
	<p>
		 1. Дисциплинарное производство может быть возбуждено общим собранием (конференцией) членов территориальной коллегии адвокатов, советом коллегии адвокатов, председателем (в его отсутствие - заместителем председателя) коллегии адвокатов или Министром юстиции по собственной инициативе либо на основании жалобы (представления, определения).
	</p>
	<p>
		 2. До возбуждения дисциплинарного производства предварительно проверяются сведения о дисциплинарном проступке адвоката путем получения от него и иных лиц письменных объяснений, истребования необходимых материалов и ознакомления с ними. Отказ адвоката от дачи письменного объяснения не является препятствием к возбуждению дисциплинарного производства.
	</p>
	<p>
		 3. Возбуждение дисциплинарного производства Министром юстиции оформляется приказом. В приказе указываются основания для возбуждения дисциплинарного производства, а также обязательные для совета коллегии адвокатов сроки рассмотрения дела о дисциплинарной ответственности.
	</p>
	<p>
		 Приказ направляется с необходимыми материалами в совет коллегии адвокатов. До принятия решения по делу о дисциплинарной ответственности приказ о возбуждении дисциплинарного производства может быть отозван Министром юстиции.
	</p>
	<p>
		 4. При необходимости советом коллегии адвокатов проводится дополнительная проверка оснований для привлечения адвоката к дисциплинарной ответственности.
	</p>
	<p>
