	</p>
	<p>
		 нарушения требований пункта 4 статьи 18 настоящего Закона;
	</p>
	<p>
		 невозможности выполнения адвокатом своих профессиональных обязанностей вследствие недостаточной квалификации, подтвержденной решением Квалификационной комиссии;
	</p>
	<p>
		 прекращения гражданства Республики Беларусь;
	</p>
	<p>
		 вступления в законную силу приговора суда о признании адвоката виновным в совершении умышленного преступления;
	</p>
	<p>
		 отказа от оказания юридической помощи в случаях, когда оказание такой помощи в соответствии с законодательством является обязательным, по основаниям, не предусмотренным нормативными правовыми актами;
	</p>
	<p>
		 систематического (два и более раза в течение 12 месяцев подряд) неотчисления или отчисления не в полном объеме средств в форме взносов на содержание территориальной коллегии адвокатов без уважительных причин.
	</p>
