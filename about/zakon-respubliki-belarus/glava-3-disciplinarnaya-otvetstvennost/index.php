	</p>
	<p>
		 Днем обнаружения дисциплинарного проступка считается день, когда о совершении проступка стало известно органу (должностному лицу), который имеет право возбуждения дисциплинарного производства.
	</p>
	<p>
		 2. Дисциплинарное производство не подлежит возбуждению, а возбужденное дисциплинарное производство подлежит прекращению, если со дня совершения дисциплинарного проступка истекло шесть месяцев, не считая периода временной нетрудоспособности адвоката, пребывания его в отпуске, времени нахождения дела в дисциплинарном производстве, а по результатам проверки, проведенной компетентными государственными органами или иными организациями, - если со дня совершения дисциплинарного проступка истекло два года.
	</p>
	<p>
		 3. Если адвокат в течение одного года со дня применения дисциплинарного взыскания не совершит нового дисциплинарного проступка, он считается не подвергавшимся дисциплинарному взысканию.
	</p>
	<p>
		 4. По решению совета коллегии адвокатов с согласия органа (должностного лица), возбудившего дисциплинарное производство, может быть произведено досрочное снятие дисциплинарного взыскания, но не ранее чем через шесть месяцев со дня его применения.
	</p>
	<p>
		<b>Статья 24. Исключение адвоката из территориальной коллегии адвокатов</b><br>
	</p>
	<p>
		1. Исключение адвоката из территориальной коллегии адвокатов производится в случаях аннулирования или прекращения действия лицензии, а также в случаях, предусмотренных пунктом 2 настоящей статьи.<br>
	</p>
