		 1. Юридическая помощь оказывается адвокатами на основании договора на оказание юридической помощи.
	</p>
	<p>
		 2. Договор, предметом которого является оказание юридической помощи, заключается между адвокатом и клиентом или иным лицом, действующим в интересах клиента, в письменной форме. Письменная форма договора должна соответствовать требованиям гражданского законодательства о письменной форме сделки. При даче устной консультации договор на оказание юридической помощи в письменной форме не заключается.
	</p>
	<p>
		 3. Договор, заключенный в письменной форме, составляется и подписывается в двух экземплярах. Один экземпляр передается клиенту (иному лицу, действующему в интересах клиента), второй хранится у адвоката, в юридической консультации.
	</p>
	<p>
		 4. Существенными условиями договора на оказание юридической помощи являются:
