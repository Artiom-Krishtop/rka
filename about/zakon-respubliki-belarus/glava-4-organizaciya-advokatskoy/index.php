 <b>Статья 29. Организация осуществления адвокатской деятельности. Юридические консультации</b>
	</p>
	<p>
		 1. Адвокатская деятельность не является предпринимательской.
	</p>
	<p>
		 2. Адвокаты осуществляют адвокатскую деятельность в адвокатском образовании в форме юридической консультации.
	</p>
	<p>
		 3. Юридические консультации являются организационной формой осуществления адвокатской деятельности и образуются территориальными коллегиями адвокатов по согласованию с Министерством юстиции в районах, городах, районах в городах для оказания юридической помощи физическим и юридическим лицам и обеспечения ее доступности. Юридические консультации не являются юридическими лицами.
	</p>
	<p>
		 4. Юридические консультации обеспечивают оказание на территории соответствующей административно-территориальной или территориальной единицы юридической помощи по уголовным делам по назначению через территориальную коллегию адвокатов по требованию органа, ведущего уголовный процесс, а также других видов юридической помощи, оказываемой за счет средств коллегий адвокатов, республиканского и (или) местного бюджетов, и несут ответственность за ее доступность, если иное не установлено территориальной коллегией адвокатов.
	</p>
	<p>
		 5. Порядок деятельности юридических консультаций определяет Белорусская республиканская коллегия адвокатов по согласованию с Министерством юстиции. Материально-техническое, финансовое и кадровое обеспечение юридических консультаций осуществляют территориальные коллегии адвокатов.
	</p>
	<p>
		 6. Руководство деятельностью юридической консультации осуществляется заведующим, назначаемым из числа адвокатов юридической консультации сроком на четыре года.
	</p>
	<p>
		 Положение о заведующем юридической консультацией утверждается Министерством юстиции.
