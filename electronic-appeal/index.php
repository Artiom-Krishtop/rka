</p>
 <span style="font-size: 11pt; color: #000000;">
Граждане (индивидуальные предприниматели) и юридические лица направляют электронные обращения с использованием специальной формы, размещенной на официальном сайте Белорусской республиканской коллегии адвокатов в глобальной компьютерной сети Интернет (</span><a href="http://www.rka.by"><span style="font-size: 11pt; color: #000000;">www.rka.by</span></a><span style="font-size: 11pt; color: #000000;">) в рубрике «Электронные обращения».</span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
Электронные обращения излагаются на белорусском или русском языке.</span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
Электронные обращения граждан (индивидуальных предпринимателей) должны содержать: </span><br>
 <span style="font-size: 11pt; color: #000000;">
наименование и (или) адрес организации либо должность лица, направляющего обращение; </span><br>
 <span style="font-size: 11pt; color: #000000;">
фамилию, собственное имя, отчество (если таковое имеется) либо инициалы гражданина, адрес его места жительства (места пребывания); </span><br>
 <span style="font-size: 11pt; color: #000000;">
изложение сути обращения; </span><br>
 <span style="font-size: 11pt; color: #000000;">
адрес электронной почты. </span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
Электронные обращения юридических лиц должны содержать:</span><br>
 <span style="font-size: 11pt; color: #000000;">
наименование и (или) адрес организации либо должность лица, направляющего обращение; </span><br>
 <span style="font-size: 11pt; color: #000000;">
полное наименование юридического лица и его место нахождения;</span><br>
 <span style="font-size: 11pt; color: #000000;">
изложение сути обращения; </span><br>
 <span style="font-size: 11pt; color: #000000;">
фамилию, собственное имя, отчество (если таковое имеется) либо инициалы руководителя или лица, уполномоченного в установленном порядке подписывать обращения; </span><br>
 <span style="font-size: 11pt; color: #000000;">
адрес электронной почты. </span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
К электронным обращениям, подаваемым представителями заявителей, должны прилагаться электронные копии документов, подтверждающих их полномочия, а также электронные копии документов о результатах предыдущего рассмотрения обращений, других документов и (или) сведений, необходимых для решения вопросов, изложенных в обращениях. </span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
В случае несоблюдения вышеперечисленных требований электронное обращение может быть оставлено без рассмотрения по существу. При оставлении обращения без рассмотрения по существу заявитель в течение пяти рабочих дней письменно уведомляется об оставлении обращения без рассмотрения по существу с указанием причин принятия такого решения. </span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
Электронные обращения заявителей, в которых не указаны фамилия, собственное имя, отчество (если таковое имеется) либо инициалы гражданина или адрес его места жительства (места пребывания) либо наименование юридического лица (полное или сокращенное) или его место нахождения либо указанные данные не соответствуют действительности, признаются анонимными и не подлежат рассмотрению, если они не содержат сведений о готовящемся, совершаемом или совершенном преступлении. </span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
Заявитель вправе: </span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
отозвать свое электронное обращение до рассмотрения его по существу путем подачи письменного заявления либо направления заявления в электронной форме тем же способом, которым было направлено электронное обращение; </span><br>
 <span style="font-size: 11pt; color: #000000;">
обжаловать в установленном порядке ответы на электронные обращения и решения об оставлении обращений без рассмотрения по существу.</span><br>
 <span style="font-size: 11pt; color: #000000;"> </span><br>
 <span style="font-size: 11pt; color: #000000;">
