					<p>
						 Адвокатура в Северо-западном крае Российской империи была учреждена Судебными уставами 1864 г. и являлась прогрессивным институтом в системе юстиции того времени. Она представляла собой корпорацию лиц свободной профессии (сословие присяжных поверенных), объединенных внутренним самоуправлением в виде выборных органов - советов присяжных поверенных и внешним надзором высших судебных мест - судебных палат и Правительствующего сената. Помимо защиты по уголовным делам (в том числе по назначению суда), представительства сторон в гражданском процессе, на присяжных поверенных возлагалось оказание правовой помощи населению, включая бесплатные консультации для бедных.
					</p>
