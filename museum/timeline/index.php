							<p>
								 Во Франции адвокаты составляли часть «парламентского корпуса» и занимали следующее за судьями место. Парламент нередко приглашал их в свои заседания для замещения отсутствующих членов и давал им судебные поручения, назначая их в качестве третейских судей для решения дел суммарным порядком или предоставляя им производство следствия, исполнение завещания и т. п. Подобно членам парламента, адвокаты пользовались высоким общественным уважением.
							</p>
							<p>
								 Право на занятие судебных должностей было лучшей привилегией французских адвокатов в средние века, а перспектива достигнуть высших мест в магистратуре — самым побудительным стимулом исполнению профессиональных обязанностей. Кроме этой привилегии, адвокаты в течение долгого времени пользовались еще другой. Она заключалась в особой подсудности: личные дела адвоката были подведомственны только палатам парламента. Несмотря на то, что несколько раз делались попытки лишить адвокатов привилегированной подсудности, она продолжала существовать вплоть до XVI в., а в несколько ограниченном виде еще и позже.
